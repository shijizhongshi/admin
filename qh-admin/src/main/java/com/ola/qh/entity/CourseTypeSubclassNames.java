package com.ola.qh.entity;

public class CourseTypeSubclassNames {

	private String id;

	private String miniSubclassName;

	private String courseTypeSubclassId;

	private String comments;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getMiniSubclassName() {
		return miniSubclassName;
	}

	public void setMiniSubclassName(String miniSubclassName) {
		this.miniSubclassName = miniSubclassName;
	}

	public String getCourseTypeSubclassId() {
		return courseTypeSubclassId;
	}

	public void setCourseTypeSubclassId(String courseTypeSubclassId) {
		this.courseTypeSubclassId = courseTypeSubclassId;
	}

	public String getComments() {
		return comments;
	}

	public void setComments(String comments) {
		this.comments = comments;
	}

}
